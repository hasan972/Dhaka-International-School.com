International curriculum
Students in this curriculum are prepared for Edexcel IGCSE and IAL exams. Subjects taught include Bengali, English, Mathematics B, Further Pure Mathematics, ICT, Physics, Chemistry, Biology,Human Biology, Accounting, Economics and Commerce. Students select at least six subjects including the compulsory English, Bengali and Mathematics B, and are allowed to sit for their IGCSEs after completing Class 9, but most prefer sitting for the exams after completing Class 10.

The school started A level courses in October 2010.</p>
