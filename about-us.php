National curriculum
The school started the national curriculum in 2006 also known as bangla medium, In this curriculum the students are prepared for giving the Secondary School Certificate(S.S.C) examinations. The students of Class 5 and 8 give "somaponi porikha". The students follow the textbooks, translated in English, of and published by National Board of Intermediate and Secondary Education in Bangladesh. The first S.S.C batch of BIS passed their examinations in 2008.After the S.S.C exam of 2010,BIS was ranked in the top 20 schools of Bangladesh.The first H.S.C national curriculum batch of BIS Started in 2011 with 15 students.The Batch was inaugurated on 17 September 2011 and the school was renamed Bangladesh International School And College.

Houses
In March 2008 the school established four houses, each of which are named after historic Bengali figures and are assigned different colors to represent them. The students are required to wear colored shoulder badges to identify themselves according to their houses. The houses have their own prefects, and are planned to have permanent sports teams, which would then participate in inter-house sports competitions[citation needed]. The houses are:

Dr. Muhammad Shahidullah House (Red)
Dr. Muhammad Qudrat-i-Khuda House (Orange)
Birshreshto Matiur Rahman House (Blue)
Birshreshto Mohiuddin Jahangir House (Green)</p>
